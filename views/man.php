	<script type="text/javascript">
		$('.music .off').click(function(event) {
			$('.music .off').fadeOut(300);
			$('.music .on').fadeIn(300);
		});

		$('.music .on').click(function(event) {
			$('.music .on').fadeOut(300);
			$('.music .off').fadeIn(300);
		});

		var time_vis;
		var interaction = 1;
		var slide_change;
		function autoplay() {
			var load = $('.control-btn.active').index();
			if (load == 3) {
				var next_slide = 0;
			} else {
				var next_slide = load+1;
			}
			stop_slide(load);
			change_slide(next_slide);
		}

		function change_slide (slide) {
			$('.control-btn:eq('+slide+')').addClass('active');
			$('.slider .next').css('background-image', $('.slider .active').css('background-image'));
			$('.slider .next').css('opacity', '1');
			$('.slider .active').css('background-image', 'url(/template/images/man/slider/'+slide+'.jpg)');
			$('.slider .next').animate({ opacity: '0' }, 600, 'easeOutCubic');
			$('.slider-text').animate({ opacity: '0' }, 300, 'easeOutCubic', function() {
				$('.slider-text').html($('.control-btn.active').attr('text'));
				$('.slider-text').animate({ opacity: '1' }, 600, 'easeOutCubic');
			});
			time_vis = setInterval(function () {
				$('.control-btn.active .status').css('width', interaction+"%");
				interaction++;
			}, 80);
			slide_change = setTimeout(autoplay, 8000);
		}

		function stop_slide () {
			clearInterval(time_vis);
			interaction = 1;
			clearTimeout(slide_change);
			$('.control-btn.active .status').css('width', '0');
			$('.control-btn.active').removeClass('active');
		}

		$(window).load(function() {
			change_slide(0);
		});

		$('.control-btn').click(function(event) {
			if (!$(this).hasClass('active')) {
				var load = $(this).index();
				stop_slide(load);
				change_slide(load);
			}
		});

		$('.slider .to-left').click(function(event) {
			var load = $('.control-btn.active').index();
			if (load == 0) {
				var next_slide = 3;
			} else {
				var next_slide = load-1;
			}
			stop_slide(load);
			change_slide(next_slide);
			return false;
		});

		$('.slider .to-right').click(function(event) {
			var load = $('.control-btn.active').index();
			if (load == 3) {
				var next_slide = 0;
			} else {
				var next_slide = load+1;
			}
			stop_slide(load);
			change_slide(next_slide);
			return false;
		});

		$('.types .square-block').hover(function() {
			if ($(window).width() > 768) {
				var load = $(this).attr('load');
				$('.types .square-block[load='+load+'] .content').css('opacity', '1');
			}
		}, function() {
			if ($(window).width() > 768) {
				$('.types .content').css('opacity', '0');
			}
		});

		$(window).on('load scroll', function(event) {
			$('.man-header').css({
				'opacity': 1-(1/$('.man-header').height()*$(window).scrollTop()),
				'background-position-y': (($(window).scrollTop()/2))+'px'
			});

			if ($(window).width() > 1450) {
				if ($(window).scrollTop() > ($('.quote-bg').offset().top-$(window).height())) {
					$('.quote-bg').css('background-position-y', (0-(($(window).scrollTop()-($('.quote-bg').offset().top-$(window).height()))))/5+'px');
				}
			} else {
				$('.quote-bg').css('background-position-y', 0);
			}
		});

		$(window).on('load resize', function(event) {
			var nav_prototype = $('.nav_prototype').width();
			$('.nav-blocks .square-block, .nav-blocks .width-block').css('height', nav_prototype+'px');
			$('.nav-blocks .heigh-block').css('height', (nav_prototype*2+15)+'px');
			if ($(window).width() > 768) {
				$('.nav-blocks').css('margin-top', (0-(nav_prototype*0.7)-7.5)+'px');
			} else {
				$('.nav-blocks').css('margin-top', -15+'px');
			}
			if ($(window).width() < 768) {
				var type_prototype = $('.type_prototype').width() - 80;
			} else {
				var type_prototype = $('.type_prototype').width();
			}
			$('.types .square-block').css('height', type_prototype+'px');
			$('.auto-margin').each(function () {
				$(this).css('margin-top', ($(this).parent().height() - $(this).height())/2+'px');
			});
			$('.steps p:eq(0), .steps p:eq(3)').css('height', ($('.steps img:eq(0)').height()+$('.steps img:eq(1)').height())-($('.steps img:eq(1)').height()*0.30)+'px');
			$('.steps p:eq(1), .steps p:eq(4)').css('height', $('.steps img:eq(1)').height()/1.2+'px');
			if ($(window).width() < 769) {
				$('.types .content').css('opacity', '1');
			} else {
				$('.types .content').css('opacity', '0');
			}
		});
	</script>

